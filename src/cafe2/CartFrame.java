    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {

            String pid = jTextField1.getText();
            String total = jTextField5.getText();
            String cname = jTextField6.getText();
            String subtotal = jTextField4.getText();
            String cash = jTextField7.getText();
            String tax = jTextField3.getText();
            String dateTime = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss").format(new Date());

            InputStream s = this.getClass().getResourceAsStream("/reports/report15.jasper");

            HashMap<String, Object> parametes = new HashMap<>();
            parametes.put("Parameter1", cname);
            parametes.put("Parameter2", dateTime);
            parametes.put("Parameter3", "Cash Only");
            parametes.put("Parameter4", pid);
            parametes.put("Parameter5", dateTime);
            parametes.put("Parameter6", tax);
            parametes.put("Parameter7", subtotal);
            parametes.put("Parameter8", cash);
            parametes.put("Parameter9", total);

            //JREmptyDataSource dataSource = new JREmptyDataSource();
            Products products1 = new Products();
            products1.setX(pid);
            products1.setY(cname);
            products1.setZ(total);

            Vector<Products> vector = new Vector<>();
            vector.add(products1);

            JRBeanCollectionDataSource dataSource = new JRBeanCollectionDataSource(vector);

            JasperPrint report = JasperFillManager.fillReport(s, parametes, dataSource);

            JasperViewer.viewReport(report, false);

            //JasperPrintManager.printReport(report, false);
            JasperExportManager.exportReportToHtmlFile(report, "Report.html");
            JasperExportManager.exportReportToPdfFile(report, "Report.pdf");

        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    public void cash() {
        try {
            double cash = Double.parseDouble(jTextField7.getText().trim());
            double total = Double.parseDouble(jTextField5.getText().trim());
            double change = (cash - total);
            jTextField8.setText(String.valueOf(change));

        } catch (Exception e) {
            Notifications.getInstance().setJFrame(this);
            Notifications.getInstance().show(Notifications.Type.WARNING, Notifications.Location.TOP_RIGHT, "Not enough cash entered");
            //JOptionPane.showMessageDialog(this, "Not enough cash entered", "Warning", 2);
        }

    }

    public boolean check() {
        if (jTextField6.getText().isEmpty()) {
            Notifications.getInstance().setJFrame(this);
            Notifications.getInstance().show(Notifications.Type.WARNING, Notifications.Location.TOP_RIGHT, "Not enough cash entered");
            //JOptionPane.showMessageDialog(this, "Please enter the customer name..", "Warning", 2);
            return false;
        }
        if (jTextField7.getText().isEmpty()) {
            Notifications.getInstance().setJFrame(this);
            Notifications.getInstance().show(Notifications.Type.WARNING, Notifications.Location.TOP_RIGHT, "Please enter the Cash amount");
            //JOptionPane.showMessageDialog(this, "Please enter the Cash amount", "Warning", 2);
            return false;
        }
        double change = Double.parseDouble(jTextField8.getText().trim());
        if (change < 0.0) {
            Notifications.getInstance().setJFrame(this);
            Notifications.getInstance().show(Notifications.Type.WARNING, Notifications.Location.TOP_RIGHT, "Not enough cash entered");
          //  JOptionPane.showMessageDialog(this, "Not enough cash entered", "Warning", 2);
            return false;
        }
        return true;
    }

