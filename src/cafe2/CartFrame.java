    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed

        model = (DefaultTableModel) jTable1.getModel();
        String proName = "";
        String proId = "";
        for (int i = 0; i < model.getRowCount(); i++) {
            proId += model.getValueAt(i, 1).toString() + ",";
            proName += model.getValueAt(i, 2).toString() + ",";
        }

        int pid = dao.getMaxRowPaymentTable() + 1;
        String cName = jTextField6.getText().trim();
        double t = Double.parseDouble(jTextField5.getText().trim());

        Payment payment = new Payment();
        payment.setPid(pid);
        payment.setcName(cName);
        payment.setProId(proId);
        payment.setProName(proName);
        payment.setTotal(t);
        payment.setDate(jTextField2.getText().trim());
        if (check()) {
            if (dao.insertPayment(payment)) {
                Notifications.getInstance().setJFrame(this);
                Notifications.getInstance().show(Notifications.Type.SUCCESS, Notifications.Location.TOP_RIGHT, "Payment Successful.");
                //  JOptionPane.showMessageDialog(this, "Payment Successful.");
                int cid = Integer.parseInt(model.getValueAt(rowIndex, 0).toString());
                dao.deleteCart(cid);

                // int x = JOptionPane.showMessageDialog(this, "Do you want to print the receipt?", "Print", JOptionPane.YES_NO_OPTION, 0);
                int x = JOptionPane.showConfirmDialog(this, "Do you want to print the receipt?", "Print", JOptionPane.YES_NO_OPTION);
                if (x == JOptionPane.YES_OPTION) {
                    try {
                        MessageFormat header = new MessageFormat("**Royal_Cafe**" + "Name:" + cName + "  " + "Total(Rs): " + t);
                        MessageFormat footer = new MessageFormat("Page{0, number,integer}");
                        jTable1.print(JTable.PrintMode.FIT_WIDTH, header, footer);
                        setVisible(false);
                    } catch (PrinterException ex) {
                        Notifications.getInstance().setJFrame(this);
                        Notifications.getInstance().show(Notifications.Type.SUCCESS, Notifications.Location.TOP_RIGHT, ex.getMessage());
                        //  JOptionPane.showMessageDialog(null, ex.getMessage());
                    }
                } else {
                    setVisible(false);
                }
            } else {
                Notifications.getInstance().setJFrame(this);
                Notifications.getInstance().show(Notifications.Type.WARNING, Notifications.Location.TOP_RIGHT, "Payment Faild!");
                //JOptionPane.showMessageDialog(this, "Payment Faild!", "Warning", 2);
            }
        }

    }//GEN-LAST:event_jButton4ActionPerformed

