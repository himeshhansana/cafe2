    private void jTextField2KeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField2KeyTyped
        char input = evt.getKeyChar();
        if (!(input < '0' || input > '9') && input != '\b') {
            evt.consume();
            Notifications.getInstance().setJFrame(this);
            Notifications.getInstance().show(Notifications.Type.WARNING, Notifications.Location.TOP_RIGHT, "Username doesn't contain any number!");
            //JOptionPane.showMessageDialog(this, "Username doesn't contain any number!", "Warning", 2);

        }
    }//GEN-LAST:event_jTextField2KeyTyped

