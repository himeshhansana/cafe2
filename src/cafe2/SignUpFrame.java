    public boolean isEmpty() {
        if (jTextField2.getText().isEmpty()) {
            Notifications.getInstance().setJFrame(this);
            Notifications.getInstance().show(Notifications.Type.WARNING, Notifications.Location.TOP_RIGHT, "Username is requied");
            // JOptionPane.showMessageDialog(this, "Username is requied", "Warning", 2);
            return false;
        }
        if (String.valueOf(jPasswordField1.getPassword()).isEmpty()) {
            Notifications.getInstance().setJFrame(this);
            Notifications.getInstance().show(Notifications.Type.WARNING, Notifications.Location.TOP_RIGHT, "Password is requied!");
            //JOptionPane.showMessageDialog(this, "Password is requied", "Warning", 2);
            return false;
        }
        if (jComboBox1.getSelectedIndex() == -1) {
            Notifications.getInstance().setJFrame(this);
            Notifications.getInstance().show(Notifications.Type.WARNING, Notifications.Location.TOP_RIGHT, "Security Question is requied");
            // JOptionPane.showMessageDialog(this, "Security Question is requied", "Warning", 2);
            return false;
        }
        if (jTextField4.getText().isEmpty()) {
            Notifications.getInstance().setJFrame(this);
            Notifications.getInstance().show(Notifications.Type.WARNING, Notifications.Location.TOP_RIGHT, "Answer is requied");
            // JOptionPane.showMessageDialog(this, "Answer is requied", "Warning", 2);
            return false;
        }
        return true;
    }

