    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed

        if (isEmpty()) {
            String username = jTextField2.getText().trim();
            if (!dao.isAdminNameExist(username)) {
                Admin admin = new Admin();
                admin.setId(dao.getMaxRowAdminTable());
                admin.setUsername(username);
                admin.setPassword(String.valueOf(jPasswordField1.getPassword()));
                admin.setsQues(jComboBox1.getSelectedItem().toString());
                admin.setAns(jTextField4.getText().trim());

                if (dao.insert(admin)) {
                    Notifications.getInstance().setJFrame(this);
                    Notifications.getInstance().show(Notifications.Type.SUCCESS, Notifications.Location.TOP_RIGHT, "Admin Successfully Created..");
                    // JOptionPane.showMessageDialog(this, "Admin Successfully Created...");
                    new LoginFrame().setVisible(true);
                    setVisible(false);
                } else {
                    Notifications.getInstance().setJFrame(this);
                    Notifications.getInstance().show(Notifications.Type.WARNING, Notifications.Location.TOP_RIGHT, "Failed!!");
                    // JOptionPane.showMessageDialog(this, "Failed!!", "Warning", 2);
                }
            } else {
                Notifications.getInstance().setJFrame(this);
                Notifications.getInstance().show(Notifications.Type.WARNING, Notifications.Location.TOP_RIGHT, "Username already exists..!");
                // JOptionPane.showMessageDialog(this, "Username already exists..!", "Warning", 2);
            }
        }
    }//GEN-LAST:event_jButton4ActionPerformed

