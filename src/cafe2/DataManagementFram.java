    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

        String DBName3 = jTextField3.getText();
        String MysqlPassword3 = jTextField4.getText();

        try {
            DBCredentials db = new DBCredentials("root", MysqlPassword3, DBName3);
            boolean isSuccess = new DataPort(db).mySql().setDumpPath(jTextField1.getText()).backup(jTextField2.getText());
            System.out.println(isSuccess);
            JOptionPane.showMessageDialog(this, "Database Backup Success", "Database Management System", JOptionPane.INFORMATION_MESSAGE);
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Database Backup UnSuccess", "Database Management System", JOptionPane.WARNING_MESSAGE);
        }
        resetBackup();
    }//GEN-LAST:event_jButton3ActionPerformed


